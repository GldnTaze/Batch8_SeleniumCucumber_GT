package com.Java.day17forLoops;

public class _1_arttırmaSoruları {
    public static void main(String[] args) {

        for (int i=1 ; i<10 ; i++)  // int i=1 degisken tanimlama asamasi
                                    //i<10 donugunu sarti
                                    //i++ dongu sirasinda artma azalam
        {
            System.out.println("hello loops");  // donuste calisacak kod blogu
        }
        System.out.println(" dongunun disinda");


        for (int i= 1; i<10; i++)  // biden baslayarak onda kucuk sayilari yazdirdik
        {
            System.out.println("i="+i);
        }

        for ( int j =5 ; j<=10; j++)  // bir bir artirilarak 5 den 10 a kadar yazili mi
        {
            System.out.println("j="+j);
        }

    }
}
